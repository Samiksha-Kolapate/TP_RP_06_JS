let showPlantsList = document.getElementById("show-plantsList");

function showPlants() {
    if (showPlantsList) {
        for (let i = 0; i < outdoorArray.length; i++) {
            showPlantsList.innerHTML += `<li><a href=outdoor_plants.html?id=${outdoorArray[i].id} target="_blank" ><h2> ${outdoorArray[i].image}  ${outdoorArray[i].title}  </h2></a></li>`;
        }
    }
}

