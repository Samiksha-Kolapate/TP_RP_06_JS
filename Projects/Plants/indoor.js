function showPlants() {
    if (showPlantsList) {
        for (let i = 0; i < indoorArray.length; i++) {
            showPlantsList.innerHTML += `<li><a href=indoor_plants.html?id=${indoorArray[i].id} target="_blank" ><h2>${indoorArray[i].image}  ${indoorArray[i].title} </h2> </a></li>`;
        }
    }
}

